When you log in the DSVPLAY platform will know what your role is. There are four different roles for DSVPLAY users with varying permissions:
<ul>
    <li>STUDENT: navigate & play</li>
    <li>STAFF: navigate & play</li>
    <li>UPLOADER: navigate & play, upload and edit presentation settings</li>
    <li>COURSE ADMIN: navigate & play, upload, edit presentation settings and edit course settings</li>
</ul>
